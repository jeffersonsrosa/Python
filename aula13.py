nome = 'Jefferson Santos'
altura = 1.94
peso = 80
imc = peso / (altura * altura)

# Jefferson Santos tem 1.94 de altura, pesa 80 quilos e seu IMC é 2.13.
# f = formatar de forma simples e objetiva

linha_1 = f'{nome} tem {altura:,.2f} de altura, e seu peso é {peso} e seu IMC é {imc:,.2f}'

print(linha_1)