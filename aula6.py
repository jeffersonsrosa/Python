# Conversao de tipos, coercao
# Type convertion, typecastins, coercion é o ato de converter um tipo em outro
# Tipos imutaveis e primitivos: str, int, float, bool

print(int("1"), type(int('1')))
print(int('1') + 1)
print(type(float('1.2') + 1))
print(bool('')) # Quando estiver vazio e sem espaco é considerado falso (False)
print(bool(' ')) # Quando estiver com algo e espaco é considerado Verdadeiro (True)
print(str(11) + 'b')
