# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas possiveis: sim (true) ou nao (false).
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro.

print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Nao => False(Falso)
print(type(10 == 10))